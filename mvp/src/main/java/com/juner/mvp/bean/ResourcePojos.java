package com.juner.mvp.bean;

import androidx.annotation.NonNull;

public class ResourcePojos implements Comparable<ResourcePojos> {

    int id;
    int courseId;
    String name;
    String courseName;
    String courseMv;
    String courseImg;
    String status;
    String type;
    int timeLength;//时长，秒
    int pageView;
    long addTime;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCourseId() {
        return courseId;
    }

    public void setCourseId(int courseId) {
        this.courseId = courseId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCourseMv() {
        return courseMv;
    }

    public void setCourseMv(String courseMv) {
        this.courseMv = courseMv;
    }

    public String getCourseImg() {
        return courseImg;
    }

    public void setCourseImg(String courseImg) {
        this.courseImg = courseImg;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getTimeLength() {
        return timeLength;
    }

    public void setTimeLength(int timeLength) {
        this.timeLength = timeLength;
    }

    public int getPageView() {
        return pageView;
    }

    public void setPageView(int pageView) {
        this.pageView = pageView;
    }

    public long getAddTime() {
        return addTime;
    }

    public void setAddTime(long addTime) {
        this.addTime = addTime;
    }

    public String getCourseName() {
        return courseName;
    }

    public void setCourseName(String courseName) {
        this.courseName = courseName;
    }

    @Override
    public int compareTo(@NonNull ResourcePojos resourcePojos) {
        int i =  this.getId()-resourcePojos.getId();

        return i;
    }
}
