package com.juner.mvp.bean;

public class WorkIndex {


    double monthIn;
    double dayIn;
    int monthOrder;
    int dayOrder;
    int monthMember;
    int dayMember;

    public double getMonthIn() {
        return monthIn;
    }

    public void setMonthIn(double monthIn) {
        this.monthIn = monthIn;
    }

    public double getDayIn() {
        return dayIn;
    }

    public void setDayIn(double dayIn) {
        this.dayIn = dayIn;
    }

    public int getMonthOrder() {
        return monthOrder;
    }

    public void setMonthOrder(int monthOrder) {
        this.monthOrder = monthOrder;
    }

    public int getDayOrder() {
        return dayOrder;
    }

    public void setDayOrder(int dayOrder) {
        this.dayOrder = dayOrder;
    }

    public int getMonthMember() {
        return monthMember;
    }

    public void setMonthMember(int monthMember) {
        this.monthMember = monthMember;
    }

    public int getDayMember() {
        return dayMember;
    }

    public void setDayMember(int dayMember) {
        this.dayMember = dayMember;
    }
}
