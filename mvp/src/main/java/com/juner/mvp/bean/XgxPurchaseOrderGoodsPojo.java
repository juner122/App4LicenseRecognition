    String goodsStandardId;
    int number;
    String goodsPrice;

    public String getGoodsId() {
        return goodsId;
    }

    public void setGoodsId(String goodsId) {
        this.goodsId = goodsId;
    }

    public String getGoodsStandardId() {
        return goodsStandardId;
    }

